public static boolean SolveSudoku(int grid[][])
    {
        // add your code here
        //printGrid(grid);
        int n=grid.length;
        int i=0,j=0;
        outer: for( i=0;i<n;i++){
            for( j=0;j<n;j++)
                {
                    if(grid[i][j]==0){
                        break outer;
                    }
                }
        }
        //printGrid(grid);
        //System.out.println();
        
        if(i==n && j==n)
            return true;
            //System.out.print(i+" "+j);
        for(int i1=1;i1<=n;i1++){
            if(issafe(i,j,i1,n,grid)){
                grid[i][j]=i1;
            if(SolveSudoku(grid))
                return true;
            grid[i][j]=0;
            }
        }
        return false;
            
    }
    static boolean  issafe(int i,int j,int value,int n,int grid[][]){
        // to check row and column
        //System.out.print(i+" "+j+" "+ +value+"   "+n);
        for(int i1=0;i1<n;i1++){
            if(grid[i1][j]==value||grid[i][i1]==value)
                return false;
            
        }
        //to check thre inner divided matrix
            int s=(int)Math.sqrt(n);
            int rs=i-i%s;
            int cs=j-j%s;
            for(int i2=0;i2<s;i2++){
                for(int i3=0;i3<s;i3++){
                    if(grid[i2+rs][i3+cs]==value)
                    {
                        return false;
                    }
                }
            }
            
        return true;
    }
    
